import { NextRequest, NextResponse } from 'next/server';

export async function POST(request: NextRequest) {
  try {
    const { format, findings } = await request.json();

    if (!format || !findings) {
      return NextResponse.json(
        { error: 'Missing format or findings' },
        { status: 400 }
      );
    }

    if (format === 'markdown') {
      const markdown = generateMarkdown(findings);
      return new NextResponse(markdown, {
        headers: {
          'Content-Type': 'text/markdown; charset=utf-8',
          'Content-Disposition': `attachment; filename="audit-report-${new Date().toISOString().split('T')[0]}.md"`,
        },
      });
    }

    if (format === 'json') {
      return NextResponse.json(findings, {
        headers: {
          'Content-Disposition': `attachment; filename="audit-report-${new Date().toISOString().split('T')[0]}.json"`,
        },
      });
    }

    return NextResponse.json(
      { error: 'Invalid format' },
      { status: 400 }
    );
  } catch (error) {
    console.error('Report generation error:', error);
    return NextResponse.json(
      { error: 'Report generation failed' },
      { status: 500 }
    );
  }
}

function generateMarkdown(findings: any) {
  const timestamp = new Date().toISOString().split('T')[0];
  
  return `# Solidity Vulnerability Detection Audit Report
**Generated:** ${timestamp}

## Executive Summary
- **Total Vulnerabilities Analyzed:** ${findings.length}
- **Average Detection Rate:** ${(findings.reduce((sum: number, f: any) => sum + f.detectionRate, 0) / findings.length * 100).toFixed(1)}%

## Detailed Findings

| SWC | Class | Severity | Detection Rate | Best Model | Recommendation |
|-----|-------|----------|----------------|-----------|-----------------|
${findings.map((f: any) => `| ${f.swc} | ${f.name} | ${f.severity} | ${(f.detectionRate * 100).toFixed(1)}% | ${f.bestModel} | ${f.recommendation} |`).join('\n')}

## Audit Strategy
1. **Critical:** Use multi-model consensus
2. **High:** Use primary + secondary model
3. **Medium:** Single model + static analyzer
4. **False Positives:** Manual review for low-confidence findings

---
*Report generated by SC Audit Benchmark*
`;
}
