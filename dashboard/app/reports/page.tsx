'use client';

import { useEffect, useState } from 'react';
import { Download, FileJson, FileText } from 'lucide-react';
import { cn } from '@/lib/utils';

interface ReportData {
  timestamp: string;
  totalModels: number;
  totalVulnerabilities: number;
  avgDetectionRate: number;
  costLeader: string;
  qualityLeader: string;
  consensusBreakdown: Record<string, number>;
  findings: {
    swc: string;
    name: string;
    severity: 'Critical' | 'High' | 'Medium';
    detectionRate: number;
    bestModel: string;
    avgCost: number;
    recommendation: string;
  }[];
}

export default function ReportsPage() {
  const [reportData, setReportData] = useState<ReportData | null>(null);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    const loadPresentationData = async () => {
      try {
        const response = await fetch('/data/presentation.json');
        const data = await response.json();
        
        const findings = Object.entries(data.leaderboard_scores || {}).map(([swc, scores]: any) => ({
          swc,
          name: data.swc_names?.[swc] || swc,
          severity: scores.severity || 'Medium',
          detectionRate: scores.detection_rate || 0,
          bestModel: data.best_model_by_swc?.[swc] || 'N/A',
          avgCost: scores.avg_cost || 0,
          recommendation: getRecommendation(scores.detection_rate || 0),
        }));

        setReportData({
          timestamp: new Date().toISOString().split('T')[0],
          totalModels: data.models?.length || 3,
          totalVulnerabilities: findings.length,
          avgDetectionRate: findings.reduce((sum, f) => sum + f.detectionRate, 0) / findings.length,
          costLeader: data.models?.[0]?.name || 'Qwen3-Coder 480B',
          qualityLeader: data.models?.[0]?.name || 'Qwen3-Coder 480B',
          consensusBreakdown: {
            'All 3 Models': findings.filter(f => f.detectionRate > 0.9).length,
            '2 of 3 Models': findings.filter(f => f.detectionRate > 0.6 && f.detectionRate <= 0.9).length,
            '1 Model Only': findings.filter(f => f.detectionRate > 0.2 && f.detectionRate <= 0.6).length,
            'None Caught': findings.filter(f => f.detectionRate <= 0.2).length,
          },
          findings,
        });
      } catch (error) {
        console.error('Failed to load presentation data:', error);
        setReportData(null);
      } finally {
        setLoading(false);
      }
    };

    loadPresentationData();
  }, []);

  const getRecommendation = (detectionRate: number) => {
    if (detectionRate > 0.8) return 'Primary detection method recommended';
    if (detectionRate > 0.6) return 'Use in combination with other tools';
    if (detectionRate > 0.3) return 'Secondary role; requires manual review';
    return 'High false-negative rate; manual audit critical';
  };

  const generateMarkdownReport = () => {
    if (!reportData) return '';

    const markdown = `# Solidity Vulnerability Detection Audit Report
**Generated:** ${reportData.timestamp}

## Executive Summary
- **Total Vulnerabilities Analyzed:** ${reportData.totalVulnerabilities} SWC classes
- **Models Evaluated:** ${reportData.totalModels}
- **Average Detection Rate:** ${(reportData.avgDetectionRate * 100).toFixed(1)}%
- **Cost-Optimized Leader:** ${reportData.costLeader}
- **Quality Leader:** ${reportData.qualityLeader}

## Model Consensus Breakdown
${Object.entries(reportData.consensusBreakdown).map(([consensus, count]) => `- **${consensus}:** ${count} vulnerabilities`).join('\n')}

## Detailed Findings

| SWC | Class | Severity | Detection Rate | Best Model | Recommendation |
|-----|-------|----------|----------------|-----------|-----------------|
${reportData.findings.map(f => `| ${f.swc} | ${f.name} | ${f.severity} | ${(f.detectionRate * 100).toFixed(1)}% | ${f.bestModel} | ${f.recommendation} |`).join('\n')}

## Audit Strategy Recommendations
1. **Critical Vulnerabilities:** Use multi-model consensus (all 3 models must agree)
2. **High Severity:** Use primary + secondary model combination
3. **Medium Severity:** Single model sufficient with static analyzer cross-check
4. **False Positive Mitigation:** Enable manual review for low-confidence findings

---
*Report generated by SC Audit Benchmark*
`;

    return markdown;
  };

  const generateJSONReport = () => {
    return JSON.stringify(reportData, null, 2);
  };

  const downloadReport = (format: 'markdown' | 'json') => {
    const content = format === 'markdown' ? generateMarkdownReport() : generateJSONReport();
    const filename = `audit-report-${reportData?.timestamp}.${format === 'markdown' ? 'md' : 'json'}`;
    
    const element = document.createElement('a');
    element.setAttribute('href', `data:text/${format === 'markdown' ? 'plain' : 'json'};charset=utf-8,${encodeURIComponent(content)}`);
    element.setAttribute('download', filename);
    element.style.display = 'none';
    document.body.appendChild(element);
    element.click();
    document.body.removeChild(element);
  };

  if (loading) {
    return (
      <div className="section-shell py-12">
        <div className="animate-pulse space-y-4">
          <div className="h-8 bg-stone-200 rounded w-64"></div>
          <div className="h-4 bg-stone-200 rounded w-full"></div>
        </div>
      </div>
    );
  }

  if (!reportData) {
    return (
      <div className="section-shell py-12">
        <div className="dark-panel">
          <p className="text-stone-300">Unable to load benchmark data. Please run the Python pipeline first.</p>
        </div>
      </div>
    );
  }

  return (
    <div className="section-shell py-12 space-y-8">
      {/* Header */}
      <div className="space-y-2">
        <p className="eyebrow">Audit Reports</p>
        <h1 className="text-4xl font-bold text-stone-950">Professional Audit Report Generator</h1>
        <p className="text-lg text-stone-600">Convert benchmark findings into client-ready reports with CVSS scores, remediation priorities, and model consensus analysis.</p>
      </div>

      {/* Executive Summary Cards */}
      <div className="grid grid-cols-1 sm:grid-cols-2 lg:grid-cols-4 gap-4">
        <div className="shell-panel">
          <p className="metric-label">Total Vulnerabilities</p>
          <p className="metric-value">{reportData.totalVulnerabilities}</p>
        </div>
        <div className="shell-panel">
          <p className="metric-label">Avg Detection Rate</p>
          <p className="metric-value">{(reportData.avgDetectionRate * 100).toFixed(1)}%</p>
        </div>
        <div className="shell-panel">
          <p className="metric-label">Models Evaluated</p>
          <p className="metric-value">{reportData.totalModels}</p>
        </div>
        <div className="shell-panel">
          <p className="metric-label">Report Date</p>
          <p className="metric-value text-sm">{reportData.timestamp}</p>
        </div>
      </div>

      {/* Export Controls */}
      <div className="flex gap-3 flex-wrap">
        <button
          onClick={() => downloadReport('markdown')}
          className={cn(
            'inline-flex items-center gap-2 px-4 py-2 rounded-lg font-medium',
            'bg-emerald-600 hover:bg-emerald-700 text-white transition-colors focus-ring'
          )}
        >
          <FileText className="w-4 h-4" />
          Download Markdown
        </button>
        <button
          onClick={() => downloadReport('json')}
          className={cn(
            'inline-flex items-center gap-2 px-4 py-2 rounded-lg font-medium',
            'bg-stone-600 hover:bg-stone-700 text-white transition-colors focus-ring'
          )}
        >
          <FileJson className="w-4 h-4" />
          Download JSON
        </button>
      </div>

      {/* Consensus Breakdown */}
      <div className="shell-panel p-6 space-y-4">
        <h2 className="text-xl font-semibold text-stone-950">Model Consensus Breakdown</h2>
        <div className="grid grid-cols-2 sm:grid-cols-4 gap-4">
          {Object.entries(reportData.consensusBreakdown).map(([consensus, count]) => (
            <div key={consensus} className="bg-stone-50 p-4 rounded border border-stone-200">
              <p className="text-sm text-stone-600 font-medium">{consensus}</p>
              <p className="text-2xl font-bold text-stone-950 mt-1">{count}</p>
              <p className="text-xs text-stone-500 mt-1">{Math.round((count / reportData.totalVulnerabilities) * 100)}% of vulns</p>
            </div>
          ))}
        </div>
      </div>

      {/* Findings Table */}
      <div className="shell-panel overflow-x-auto">
        <table className="w-full text-sm">
          <thead className="bg-stone-50 border-b border-stone-200">
            <tr>
              <th className="px-4 py-3 text-left font-semibold text-stone-950">SWC</th>
              <th className="px-4 py-3 text-left font-semibold text-stone-950">Class</th>
              <th className="px-4 py-3 text-left font-semibold text-stone-950">Severity</th>
              <th className="px-4 py-3 text-left font-semibold text-stone-950">Detection Rate</th>
              <th className="px-4 py-3 text-left font-semibold text-stone-950">Best Model</th>
              <th className="px-4 py-3 text-left font-semibold text-stone-950">Recommendation</th>
            </tr>
          </thead>
          <tbody className="divide-y divide-stone-200">
            {reportData.findings.map((finding) => (
              <tr key={finding.swc} className="hover:bg-stone-50">
                <td className="px-4 py-3 font-mono text-stone-600">{finding.swc}</td>
                <td className="px-4 py-3 text-stone-900">{finding.name}</td>
                <td className="px-4 py-3">
                  <span className={cn(
                    'px-2 py-1 text-xs font-semibold rounded',
                    finding.severity === 'Critical' ? 'bg-red-100 text-red-800' :
                    finding.severity === 'High' ? 'bg-orange-100 text-orange-800' :
                    'bg-yellow-100 text-yellow-800'
                  )}>
                    {finding.severity}
                  </span>
                </td>
                <td className="px-4 py-3">
                  <div className="flex items-center gap-2">
                    <div className="w-16 h-2 bg-stone-200 rounded-full overflow-hidden">
                      <div
                        className="h-full bg-emerald-500"
                        style={{ width: `${finding.detectionRate * 100}%` }}
                      />
                    </div>
                    <span className="text-stone-900 font-medium w-12">{(finding.detectionRate * 100).toFixed(0)}%</span>
                  </div>
                </td>
                <td className="px-4 py-3 text-stone-900">{finding.bestModel}</td>
                <td className="px-4 py-3 text-stone-600 text-xs">{finding.recommendation}</td>
              </tr>
            ))}
          </tbody>
        </table>
      </div>

      {/* Strategy Recommendations */}
      <div className="shell-panel p-6 space-y-4">
        <h2 className="text-xl font-semibold text-stone-950">Audit Strategy Recommendations</h2>
        <ul className="space-y-3 text-stone-700">
          <li className="flex gap-3">
            <span className="text-emerald-600 font-bold">→</span>
            <span><strong>Critical:</strong> Multi-model consensus (all 3 models must agree)</span>
          </li>
          <li className="flex gap-3">
            <span className="text-emerald-600 font-bold">→</span>
            <span><strong>High:</strong> Primary + secondary model for confidence</span>
          </li>
          <li className="flex gap-3">
            <span className="text-emerald-600 font-bold">→</span>
            <span><strong>Medium:</strong> Single model + static analyzer cross-check</span>
          </li>
          <li className="flex gap-3">
            <span className="text-emerald-600 font-bold">→</span>
            <span><strong>False Positives:</strong> Manual review for low-confidence findings</span>
          </li>
        </ul>
      </div>
    </div>
  );
}
